     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if (Gate::allows('get-quanli')) {
            $title = "Quản lí file";
            
            $data = DB::table('file')->join('nhom','file.id_nhom','=','nhom.id_nhom')
                                    ->join('dot_thuctap','nhom.id_dot','=','dot_thuctap.id_dot')
                                    ->select('file.*','dot_thuctap.ten_dot','nhom.ten_nhom')
                                    ->where('nhom.id_dot','=',Auth::user()->id_dot)->get();
                                   
            return view('nhom.download_ql',compact('title','data'));
            } else {
                return back();
            }
    }

